""" Converters fuctions.

consist of functions to convert text to typically be used within
"""

def lower_string(value):
    """ Return the lowercase of `value` string"""
    return value.lower()


def upper_string(value):
    """ Return the lowercase of `value` string"""
    return value.upper()


def capitalize_string(value):
    """ Return the lowercase of `value` string."""
    return value.capitalize()